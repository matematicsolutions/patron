// Patron - English UI dictionary.
//
// Architecture (see ADR-0132):
// - One language per installation (law firm = one language). Locale is chosen in
//   the install config, not in the URL. Default is PL; EN is opt-in.
// - Keys MIRROR pl.ts exactly. `pl` remains the source of keys (TranslationKey is
//   derived from `pl`); any key missing here falls back to PL in `t()`.
// - Date format is handled by the `formatDate` helper (locale-aware), not in the
//   dictionary.
//
// NOTE (spec 001): legal-EN terminology pass applied (reviewer-en, 2026-06-25).
// Court names and register abbreviations expanded for an EN audience; house-style
// hyphens enforced. "Counsel" is kept as the form of address by design.

